/// Drift-based implementation of [FarmLocalDataSource].
class FarmLocalDataSourceImpl implements FarmLocalDataSource {
  final FarmDatabase _db;
  final _log = Logger('FarmLocalDataSource');

  FarmLocalDataSourceImpl({required FarmDatabase database}) : _db = database;

  @override
  Future<List<FarmModel>> getFarms(String userId) async {
    _log.fine('Loading cached farms for user $userId');
    final query = _db.select(_db.cachedFarms)
      ..where((t) => t.ownerId.equals(userId));
    final rows = await query.get();
    return rows.map((row) {
      final json = jsonDecode(row.dataJson) as Map<String, dynamic>;
      return FarmModel.fromJson(json);
    }).toList();
  }

  @override
  Future<FarmModel?> getFarmById(String farmId) async {
    final query = _db.select(_db.cachedFarms)
      ..where((t) => t.id.equals(farmId));
    final row = await query.getSingleOrNull();
    if (row == null) return null;
    final json = jsonDecode(row.dataJson) as Map<String, dynamic>;
    return FarmModel.fromJson(json);
  }

  @override
  Future<void> cacheFarms(List<FarmModel> farms) async {
    _log.fine('Caching ${farms.length} farms');
    await _db.batch((batch) {
      for (final farm in farms) {
        batch.insert(
          _db.cachedFarms,
          CachedFarmsCompanion.insert(
            id: farm.id,
            name: farm.name,
            ownerId: farm.ownerId,
            dataJson: jsonEncode(farm.toJson()),
          ),
          mode: InsertMode.insertOrReplace,
        );
      }
    });
  }

  @override
  Future<void> cacheFarm(FarmModel farm) async {
    await _db.into(_db.cachedFarms).insertOnConflictUpdate(
          CachedFarmsCompanion.insert(
            id: farm.id,
            name: farm.name,
            ownerId: farm.ownerId,
            dataJson: jsonEncode(farm.toJson()),
          ),
        );
  }

  @override
  Future<void> deleteFarm(String farmId) async {
    await (_db.delete(_db.cachedFarms)..where((t) => t.id.equals(farmId)))
        .go();
    await (_db.delete(_db.cachedFields)
          ..where((t) => t.farmId.equals(farmId)))
        .go();
  }

  @override
  Future<List<FieldModel>> getFieldsByFarmId(String farmId) async {
    final query = _db.select(_db.cachedFields)
      ..where((t) => t.farmId.equals(farmId));
    final rows = await query.get();
    return rows.map((row) {
      final json = jsonDecode(row.dataJson) as Map<String, dynamic>;
      return FieldModel.fromJson(json);
    }).toList();
  }

  @override
  Future<void> cacheField(FieldModel field) async {
    await _db.into(_db.cachedFields).insertOnConflictUpdate(
          CachedFieldsCompanion.insert(
            id: field.id,
            farmId: field.farmId,
            dataJson: jsonEncode(field.toJson()),
          ),
        );
  }

  @override
  Future<void> deleteField(String fieldId) async {
    await (_db.delete(_db.cachedFields)..where((t) => t.id.equals(fieldId)))
        .go();
  }

  @override
  Future<void> clearAll() async {
    await _db.delete(_db.cachedFarms).go();
    await _db.delete(_db.cachedFields).go();
  }
}
