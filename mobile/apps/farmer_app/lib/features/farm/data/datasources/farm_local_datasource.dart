/// Local data source for farm operations using Drift (SQLite).
abstract class FarmLocalDataSource {
  Future<List<FarmModel>> getFarms(String userId);
  Future<FarmModel?> getFarmById(String farmId);
  Future<void> cacheFarms(List<FarmModel> farms);
  Future<void> cacheFarm(FarmModel farm);
  Future<void> deleteFarm(String farmId);
  Future<List<FieldModel>> getFieldsByFarmId(String farmId);
  Future<void> cacheField(FieldModel field);
  Future<void> deleteField(String fieldId);
  Future<void> clearAll();
}

// --------------------------------------------------------------------------
// Drift table definitions
// --------------------------------------------------------------------------

/// Drift table for cached farms.
class CachedFarms extends Table {
  TextColumn get id => text()();
  TextColumn get name => text()();
  TextColumn get ownerId => text()();
  TextColumn get dataJson => text()();
  DateTimeColumn get cachedAt => dateTime().withDefault(currentDateAndTime)();

  @override
  Set<Column> get primaryKey => {id};
}

/// Drift table for cached fields.
class CachedFields extends Table {
  TextColumn get id => text()();
  TextColumn get farmId => text()();
  TextColumn get dataJson => text()();
  DateTimeColumn get cachedAt => dateTime().withDefault(currentDateAndTime)();

  @override
  Set<Column> get primaryKey => {id};
}

// --------------------------------------------------------------------------
// Drift database definition
// --------------------------------------------------------------------------

@DriftDatabase(tables: [CachedFarms, CachedFields])
class FarmDatabase extends GeneratedDatabase {
  FarmDatabase(QueryExecutor e) : super(e);

  @override
  int get schemaVersion => 1;

  @override
  MigrationStrategy get migration => MigrationStrategy(
        onCreate: (m) async {
          await m.createAll();
        },
      );

  @override
  Iterable<TableInfo<Table, dynamic>> get allTables =>
      allSchemaEntities.whereType<TableInfo<Table, dynamic>>();

  @override
  Iterable<DatabaseSchemaEntity> get allSchemaEntities {
    // Manual schema entity declarations for the two tables.
    return [cachedFarms, cachedFields];
  }

  // Table accessors
  late final $CachedFarmsTable cachedFarms = $CachedFarmsTable(this);
  late final $CachedFieldsTable cachedFields = $CachedFieldsTable(this);
}

