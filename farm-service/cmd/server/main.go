// Package main wires all farm-service layers together and starts the HTTP server.
// Dependency injection is explicit: no reflection-based DI frameworks.
//
// Pre-requisites:
//   - Run `buf generate` in farm-service/ to produce api/v1/*.pb.go and
//     api/v1/farmv1connect/*.connect.go before building this binary.
package main

import (
	"context"
	"log"
	"net/http"
	"os"
	"os/signal"
	"syscall"
	"time"

	"connectrpc.com/connect"
	"github.com/IBM/sarama"
	"github.com/jackc/pgx/v5/pgxpool"
	"go.uber.org/zap"

	"p9e.in/samavaya/packages/connect/interceptors"
	connectserver "p9e.in/samavaya/packages/connect/server"
	"p9e.in/samavaya/packages/p9log"

	// Generated by buf generate — run `buf generate` in farm-service/ first.
	farmv1connect "p9e.in/samavaya/agriculture/farm-service/api/v1/farmv1connect"

	// Inbound adapters
	grpcadapter "p9e.in/samavaya/agriculture/farm-service/internal/adapters/inbound/grpc"

	// Outbound adapters
	kafkaadapter "p9e.in/samavaya/agriculture/farm-service/internal/adapters/outbound/kafka"
	postgresadapter "p9e.in/samavaya/agriculture/farm-service/internal/adapters/outbound/postgres"

	// Application core
	"p9e.in/samavaya/agriculture/farm-service/internal/application"
)

func main() {
	// ── Logger ──────────────────────────────────────────────────────────────
	zapLogger, err := zap.NewProduction()
	if err != nil {
		log.Fatalf("failed to create logger: %v", err)
	}
	defer zapLogger.Sync() //nolint:errcheck
	logger := p9log.NewLogger(zapLogger)

	// ── Config from environment ──────────────────────────────────────────────
	dsn := mustEnv("DATABASE_URL", "postgres://localhost:5432/farm_service?sslmode=disable")
	kafkaBroker := os.Getenv("KAFKA_BROKER") // optional; events are best-effort
	port := envOr("PORT", "8080")

	// ── Database pool ────────────────────────────────────────────────────────
	ctx, cancel := context.WithTimeout(context.Background(), 15*time.Second)
	defer cancel()

	pool, err := pgxpool.New(ctx, dsn)
	if err != nil {
		log.Fatalf("failed to connect to database: %v", err)
	}
	defer pool.Close()

	if err := pool.Ping(ctx); err != nil {
		log.Fatalf("database ping failed: %v", err)
	}

	// ── Kafka producer (optional) ────────────────────────────────────────────
	var kafkaProducer sarama.SyncProducer
	if kafkaBroker != "" {
		cfg := sarama.NewConfig()
		cfg.Producer.Return.Successes = true
		kafkaProducer, err = sarama.NewSyncProducer([]string{kafkaBroker}, cfg)
		if err != nil {
			log.Printf("WARNING: failed to create Kafka producer: %v — events will be dropped", err)
		} else {
			defer kafkaProducer.Close()
		}
	}

	// ── Outbound adapters ────────────────────────────────────────────────────
	repo := postgresadapter.NewFarmRepository(pool, logger)
	pub := kafkaadapter.NewEventPublisher(kafkaProducer, logger)

	// ── Application service (core) ───────────────────────────────────────────
	svc := application.NewFarmService(repo, pub, pool, logger)

	// ── Inbound adapters ─────────────────────────────────────────────────────
	handler := grpcadapter.NewFarmHandler(svc, logger)

	// ── ConnectRPC server setup ──────────────────────────────────────────────
	mwCfg := connectserver.MiddlewareConfig{
		EnableRecovery:  true,
		EnableRequestID: true,
		EnableLogging:   true,
		EnableDB:        true,
		DBPool:          pool,
	}
	connectOpt := connectserver.NewConnectOption(mwCfg)

	mux := http.NewServeMux()
	// Register the generated ConnectRPC handler.
	// farmv1connect.NewFarmServiceHandler is generated by buf generate.
	path, farmHandler := farmv1connect.NewFarmServiceHandler(handler,
		connect.WithInterceptors(
			interceptors.RequestIDInterceptor(),
			interceptors.LoggingInterceptor(interceptors.WithLogger(*p9log.NewHelper(logger))),
		),
		connectOpt,
	)
	mux.Handle(path, farmHandler)

	// Health / readiness probes
	mux.HandleFunc("/health", func(w http.ResponseWriter, _ *http.Request) {
		w.WriteHeader(http.StatusOK)
		_, _ = w.Write([]byte(`{"status":"ok"}`))
	})
	mux.HandleFunc("/ready", func(w http.ResponseWriter, _ *http.Request) {
		if err := pool.Ping(context.Background()); err != nil {
			w.WriteHeader(http.StatusServiceUnavailable)
			return
		}
		w.WriteHeader(http.StatusOK)
		_, _ = w.Write([]byte(`{"status":"ready"}`))
	})

	serverCfg := connectserver.DefaultServerConfig(port)
	srv := connectserver.NewHTTPServer(serverCfg, mux)

	// ── Graceful shutdown ────────────────────────────────────────────────────
	quit := make(chan os.Signal, 1)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)

	go func() {
		p9log.NewHelper(logger).Infow("msg", "farm-service starting", "port", port)
		if err := srv.ListenAndServe(); err != nil && err != http.ErrServerClosed {
			log.Fatalf("server error: %v", err)
		}
	}()

	<-quit
	p9log.NewHelper(logger).Infow("msg", "shutting down farm-service")

	shutCtx, shutCancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer shutCancel()
	if err := srv.Shutdown(shutCtx); err != nil {
		log.Printf("graceful shutdown error: %v", err)
	}
}

func mustEnv(key, fallback string) string {
	if v := os.Getenv(key); v != "" {
		return v
	}
	if fallback != "" {
		return fallback
	}
	log.Fatalf("required environment variable %s is not set", key)
	return ""
}

func envOr(key, fallback string) string {
	if v := os.Getenv(key); v != "" {
		return v
	}
	return fallback
}
